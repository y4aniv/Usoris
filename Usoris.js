function Usoris(dico, opt) {
  if (!dico) {
    var dic_1 = items[Math.floor(Math.random() * items.length)];
    var dic_2 = items[Math.floor(Math.random() * items.length)];
    if (dic_1 == dic_2) {
      dic_2 = items[Math.floor(Math.random() * items.length)];
    }
    var username = [
      dictionaries[dic_1][
        Math.floor(Math.random() * dictionaries[dic_1].length)
      ],
      dictionaries[dic_2][
        Math.floor(Math.random() * dictionaries[dic_2].length)
      ],
    ];
    return Array.from(username).join("-").toLocaleLowerCase();
  }
  var usernameArray = [];
  var username = "";
  for (var i = 0; i < dico.length; i++) {
    if (!dictionaries[dico[i]]) {
      throw new Error("Dictionary not found: " + dico[i]);
      return;
    } else {
      usernameArray.push(
        dictionaries[dico[i]][
          Math.floor(Math.random() * dictionaries[dico[i]].length)
        ]
      );
    }
  }
  if (opt) {
    if (!opt.separator) {
      opt.separator = "-";
    }
    if (!opt.style) {
      opt.style = "lower";
    }
    if (
      opt.style === "lower" ||
      opt.style === "upper" ||
      opt.style == "capitalize"
    ) {
      if (opt.style == "lower") {
        username = Array.from(usernameArray)
          .join(opt.separator)
          .toLocaleLowerCase();
      }
      if (opt.style == "upper") {
        username = Array.from(usernameArray)
          .join(opt.separator)
          .toLocaleUpperCase();
      }
      if (opt.style == "capitalize") {
        function split(str, separator) {
          var split = str.toLowerCase().split(" ");
          for (var i = 0; i < split.length; i++) {
            split[i] =
              split[i].charAt(0).toUpperCase() + split[i].substring(1);
          }
          return split.join(separator);
        }
        username = Array.from(usernameArray).join(" ");
        username = split(username, opt.separator);
      }
    } else {
      throw new Error("Style not found: " + opt.style);
      return;
    }
  } else {
    username = Array.from(usernameArray).join("-").toLocaleLowerCase();
  }
  return username;
}
